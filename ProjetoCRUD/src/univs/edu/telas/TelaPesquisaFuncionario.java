    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = tabelaUsuario.getSelectedRow();
        
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else if(JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este usu치rio", "Excluir usu치rio", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            funcionario = dao.pesquisar((int) tabelaUsuario.getValueAt(linha, 0));
            dao.excluir(funcionario);
            atualizarTabela();
            JOptionPane.showMessageDialog(null, "Usu치rio excluido!");
        }

       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int linha = tabelaUsuario.getSelectedRow();
        
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else{
             funcionario = dao.pesquisar((int) tabelaUsuario.getValueAt(linha, 0));
             TelaFuncionario tela = new TelaFuncionario();
             tela.funcionario = funcionario;
             tela.preencherFuncionario();
             tela.setVisible(true);
             dispose();
        }

       
    }//GEN-LAST:event_jButton3ActionPerformed

